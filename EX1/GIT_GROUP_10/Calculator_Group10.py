def decimal_to_hex(number, precision=8):
    hex_digits = "0123456789ABCDEF"

    # Handle the sign
    sign = ""
    if number < 0:
        sign = "-"
        number = -number

    # Separate into integer part and fractional part
    integer_part = int(number)
    fractional_part = number - integer_part

    #  Convert the integer part
    if integer_part == 0:
        integer_hex = "0"
    else:
        integer_hex = ""
        while integer_part > 0:
            remainder = integer_part % 16
            integer_hex = hex_digits[remainder] + integer_hex
            integer_part //= 16

    #  Convert the fractional part
    if fractional_part == 0:
        return sign + integer_hex

    fractional_hex = ""
    count = 0

    while fractional_part > 0 and count < precision:
        fractional_part *= 16
        digit = int(fractional_part)
        fractional_hex += hex_digits[digit]
        fractional_part -= digit
        count += 1

    return sign + integer_hex + "." + fractional_hex


def convert_decimal_input_to_hex(user_input, precision=8):
    user_input = user_input.strip()

    if user_input == "":
        return "Error: empty input is not valid."

    # Check that there is at most one decimal point
    if user_input.count(".") > 1:
        return "Error: invalid decimal number format."

    # Check that the minus sign, if present, appears only at the beginning
    if user_input.count("-") > 1 or ("-" in user_input and user_input[0] != "-"):
        return "Error: invalid negative number format."

    # Check for invalid inputs like "-", ".", or "-."
    if user_input in {"-", ".", "-."}:
        return "Error: invalid numeric input."

    # Check that all characters are valid
    allowed_chars = "0123456789.-"
    for ch in user_input:
        if ch not in allowed_chars:
            return "Error: input must be a decimal number only."

    # Attempt to convert to a number
    try:
        number = float(user_input)
    except ValueError:
        return "Error: invalid numeric input."

    # Call the conversion function
    return decimal_to_hex(number, precision)

