def hex_char_to_value(ch):
    hex_digits = "0123456789ABCDEF"
    return hex_digits.index(ch)


def hex_to_decimal(hex_string):
    hex_string = hex_string.strip().upper()

    if hex_string == "":
        return "Error: empty input is not valid."

    # Handle the sign
    sign = 1
    if hex_string[0] == "-":
        sign = -1
        hex_string = hex_string[1:]

    # Basic validations after removing the sign
    if hex_string == "":
        return "Error: invalid hexadecimal input."

    if hex_string.count(".") > 1:
        return "Error: invalid hexadecimal format."

    if hex_string in {".", "-."}:
        return "Error: invalid hexadecimal input."

    # Check that all characters are valid
    allowed_chars = "0123456789ABCDEF."
    for ch in hex_string:
        if ch not in allowed_chars:
            return "Error: input must be a hexadecimal number only."

    # Split into integer and fractional parts
    parts = hex_string.split(".")
    integer_part = parts[0]
    fractional_part = parts[1] if len(parts) == 2 else ""

    # If nothing appears before the decimal point, treat it as 0
    if integer_part == "":
        integer_part = "0"

    decimal_value = 0

    # Convert the integer part
    power = 0
    for ch in reversed(integer_part):
        value = hex_char_to_value(ch)
        decimal_value += value * (16 ** power)
        power += 1

    # Convert the fractional part
    power = -1
    for ch in fractional_part:
        value = hex_char_to_value(ch)
        decimal_value += value * (16 ** power)
        power -= 1

    return sign * decimal_value

