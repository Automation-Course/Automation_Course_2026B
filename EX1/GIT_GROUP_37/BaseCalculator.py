def run_calculator():
    # הודעת שגיאה לכל הקלטים הלא תקינים
    error_msg = "Error: Invalid input. Please enter a positive whole number or a valid hexadecimal value."

    while True:
        print("\n=== Number Base Calculator (Dec <-> Hex) ===")
        print("1. Decimal to Hexadecimal")
        print("2. Hexadecimal to Decimal")
        print("3. Run System Tests")
        print("4. Exit")

        choice = input("\nPlease choose an option (1-4): ")

        if choice == '1':
            while True:
                user_input = input("Enter a decimal number: ")

                # הטיפול בקלט ריק (רק לחיצה על Enter)
                if not user_input:
                    print(error_msg)
                    continue

                try:
                    num = int(user_input)
                    if num < 0:
                        print(error_msg)
                        continue
                    print(f"Result in Hexadecimal: {decimal_to_hex(num)}")
                    break # חוזר לתפריט הראשי אחרי הצלחה
                except ValueError:
                    print(error_msg)

        elif choice == '2':
            while True:
                user_input = input("Enter a hexadecimal number: ")

                # הטיפול בקלט ריק בהמרה הקסדצימלית
                if not user_input:
                    print(error_msg)
                    continue

                if "-" in user_input:
                    print(error_msg)
                    continue
                try:
                    result = hex_to_decimal(user_input)
                    print(f"Result in Decimal: {result}")
                    break
                except (ValueError, KeyError):
                    print(error_msg)

        elif choice == '3':
            run_tests()

        elif choice == '4':
            print("\n")
            shutdown_msg = "Powering down Base Calculator..."

            # יצירת אפקט הקלדה מגניב לאותיות
            for char in shutdown_msg:
                sys.stdout.write(char)
                sys.stdout.flush()
                time.sleep(0.04)  # השהיה קטנה בין כל אות

            print("\n")
            time.sleep(0.3)

            # כיבוי מערכת
            print("System Offline. GOODBYE! \n")

            time.sleep(1.5)
            break
        else:
            print("Invalid choice, please try again.")

