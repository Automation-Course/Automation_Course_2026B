import time
import sys

# מילון שמתרגם מספרים מעל 9 לאותיות באנגלית
num_to_hex = {10: 'A', 11: 'B', 12: 'C', 13: 'D', 14: 'E', 15: 'F'}

def decimal_to_hex(num):
    # טיפול במקרה של 0
    if num == 0:
        return "0"

    hex_result = ""
    # מימוש אלגוריתם המרה באמצעות חילוק חוזר ב-16 ובניית המחרוזת מהסוף להתחלה
    while num > 0:
        remainder = num % 16
        if remainder >= 10:
            # שליפת האות המתאימה מהמילון עבור שאריות הגדולות מ-9
            hex_result = num_to_hex[remainder] + hex_result
        else:
            hex_result = str(remainder) + hex_result
        # עדכון המספר על ידי חילוק בשלמים לקראת האיטרציה הבאה
        num = num // 16
    return hex_result

# מילון שמתרגם אותיות חזרה למספרים עשרוניים
hex_to_num = {'A': 10, 'B': 11, 'C': 12, 'D': 13, 'E': 14, 'F': 15}

def hex_to_decimal(hex_str):
    # הפיכת כל המחרוזת לאותיות גדולות כדי לטפל במקרה של אותיות קטנות
    hex_str = hex_str.upper()
    decimal_result = 0
    power = 0

    # נעבור על המחרוזת מהסוף להתחלה (מימין לשמאל)
    for digit in reversed(hex_str):
        # אם התו הוא אות, ניקח את המספר המתאים מהמילון
        if digit in hex_to_num:
            value = hex_to_num[digit]
        # אם התו הוא מספר, נהפוך אותו לערך מספרי
        else:
            value = int(digit)

        # נכפיל את הערך ב-16 בחזקת המיקום שלו ונוסיף לתוצאה
        decimal_result += value * (16 ** power)
        # נקדם את החזקה ב-1 לקראת הספרה הבאה שמאלה
        power += 1
    return decimal_result

