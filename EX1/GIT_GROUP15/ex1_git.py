# Converts integer part from decimal to hexadecimal using repeated division
def convert_decimal_integer_part_to_hex(integer_part):
    integer_part = int(integer_part)

    # Special case: if number is 0
    if integer_part == 0:
        return "0"

    hex_result = ""

    # Repeat until number becomes 0
    while integer_part > 0:
        remainder = integer_part % 16 # Get remainder
        hex_digit = decimal_digit_to_hex(remainder) # Convert to hex character

        # Add digit to the left
        hex_result = hex_digit + hex_result

        integer_part = integer_part // 16 # Reduce number

    return hex_result

#Converts fractional part from decimal to hexadecimal using repeated multiplication
def convert_decimal_fraction_part_to_hex(fraction_part, max_digits=6):
    # Convert string to decimal fraction
    fraction_value = float("0." + fraction_part)
    hex_result = ""
    count = 0

    # Continue while fraction exists and limit not reached
    while fraction_value > 0 and count < max_digits:
        fraction_value *= 16 # Multiply by base
        integer_digit = int(fraction_value) # Extract integer part
        hex_result += decimal_digit_to_hex(integer_digit)
        fraction_value -= integer_digit # Keep only fractional remainder
        count += 1

    return hex_result

def convert_decimal_to_hex(decimal_number):
    # Check if number has fractional part
    if "." in decimal_number:
        parts = decimal_number.split(".")
        integer_part = parts[0]
        fraction_part = parts[1]

        # Convert each part separately
        integer_result = convert_decimal_integer_part_to_hex(integer_part)
        fraction_result = convert_decimal_fraction_part_to_hex(fraction_part)

        # If no fractional result, return only integer part
        if fraction_result == "":
            return integer_result
        else:
            return integer_result + "." + fraction_result

    else:
        return convert_decimal_integer_part_to_hex(decimal_number)

