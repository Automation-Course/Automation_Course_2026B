# Checks whether the input is valid, mode 1- decimal to hexadecimal, mode 2- hexadecimal to decimal
def is_valid_input(user_input, mode):

    # Empty input is invalid
    if user_input == "":
        return False

    # Dot at the beginning or end is invalid
    if user_input[0] == "." or user_input[-1] == ".":
        return False

    # More than one dot is invalid
    if user_input.count(".") > 1:
        return False

    if mode == 1:
        # For decimal input: only digits and dot are allowed
        for ch in user_input:
            if not (ch.isdigit() or ch == "."):
                return False

    elif mode == 2:
        # For hexadecimal input: only digits and A-F allowed
        for ch in user_input:
            if not (ch.isdigit() or ch in "ABCDEF" or ch == "."):
                return False

    else:
        return False

    return True

def hex_digit_to_decimal(ch):
    ch = ch.upper() # Convert character to uppercase to ensure uniform handling

    # If character is a digit (0-9), convert directly to integer
    if ch.isdigit():
        return int(ch)

    # Map hexadecimal letters to their decimal values
    elif ch == 'A':
        return 10
    elif ch == 'B':
        return 11
    elif ch == 'C':
        return 12
    elif ch == 'D':
        return 13
    elif ch == 'E':
        return 14
    elif ch == 'F':
        return 15

def decimal_digit_to_hex(value):
    # If value is between 0-9, return it as a string
    if 0 <= value <= 9:
        return str(value)

    # Map values 10-15 to hexadecimal characters
    elif value == 10:
        return "A"
    elif value == 11:
        return "B"
    elif value == 12:
        return "C"
    elif value == 13:
        return "D"
    elif value == 14:
        return "E"
    elif value == 15:
        return "F"


#----- Hex to Decimal------

# Converts the integer part Uses positional notation
def convert_hex_integer_part_to_decimal(integer_part):
    decimal_value = 0
    power = len(integer_part) - 1 # Highest power

    for ch in integer_part:
        digit_value = hex_digit_to_decimal(ch) # Convert hex digit to decimal value
        decimal_value += digit_value * (16 ** power) # Multiply digit by its positional value
        power -= 1

    return decimal_value

# Converts the fractional part Uses negative powers of 16
def convert_hex_fraction_part_to_decimal(fraction_part):
    decimal_value = 0
    power = -1 # Start with negative power for fractional part

    # Loop through digits after the decimal point
    for ch in fraction_part:
        digit_value = hex_digit_to_decimal(ch)
        decimal_value += digit_value * (16 ** power)
        power -= 1

    return decimal_value

#Formats decimal output to at most 'max_digits' digits after the point
def format_decimal_output(value, max_digits=6):
    value_str = str(value) # Convert number to string for manipulation

    # If there is no decimal point, return as is
    if "." not in value_str:
        return value_str

    # Split into integer and fractional parts
    integer_part, fraction_part = value_str.split(".")

    # Keep only the required number of digits after the decimal point
    fraction_part = fraction_part[:max_digits]

    # Remove unnecessary trailing zeros
    fraction_part = fraction_part.rstrip("0")

    # If nothing remains after removing zeros, return only integer part
    if fraction_part == "":
        return integer_part

    return integer_part + "." + fraction_part

def convert_hex_to_decimal(hex_number):
    # Check if number contains a decimal point and split into integer and fractional parts
    if "." in hex_number:
        parts = hex_number.split(".")
        integer_part = parts[0]
        fraction_part = parts[1]

        # Convert each part separately
        integer_value = convert_hex_integer_part_to_decimal(integer_part)
        fraction_value = convert_hex_fraction_part_to_decimal(fraction_part)

        result = integer_value + fraction_value
        # limit digits after decimal point
        return format_decimal_output(result, 6)

    else:
        # If no fractional part, convert only integer part
        return str(convert_hex_integer_part_to_decimal(hex_number))


